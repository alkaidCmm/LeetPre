package com.cmm.jzOffer._05printListReverse;

public class ListNode {
	public int value;
	public ListNode next;

	public ListNode(int value) {
		super();
		this.value = value;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public ListNode getNext() {
		return next;
	}

	public void setNext(ListNode next) {
		this.next = next;
	}
}
